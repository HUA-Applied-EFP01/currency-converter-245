# Συνάρτηση για τη μετατροπή νομισμάτων
def convert_currency(amount, from_currency, to_currency):

    # Ελέγχουμε αν τα νομίσματα είναι έγκυρα
    if from_currency not in exchange_rates or to_currency not in exchange_rates:
        return f"Το νόμισμα {from_currency} ή/και {to_currency} δεν υπάρχει. Παρακαλώ εισάγετε έγκυρα νομίσματα (π.χ. USD, EUR, GBP)."
   
    # Υπολογισμός της μετατροπής: Επειδή τα rates είναι με βάση το ευρώ, το νόμισμα θα μετατραπεί πρώτα σε ευρώ και έπειτα θα μετατραπεί στο νόμισμα που ζητείται
    euro_amount = amount / exchange_rates[from_currency]  # Μετατροπή σε EUR
    converted_amount = euro_amount * exchange_rates[to_currency]  # Μετατροπή στο τελικό νόμισμα
    return round(converted_amount, 2)


# Συνάρτηση για την αλληλεπίδραση με τον χρήστη
def user_input():
  
  while True:
    print("Καλώς ήρθατε στον μετατροπέα νομισμάτων!")
   
    # Λήψη ποσού
    try:
        amount = float(input("Εισάγετε το ποσό που θέλετε να μετατρέψετε: "))
    except ValueError:
        print("Παρακαλώ εισάγετε ένα έγκυρο αριθμητικό ποσό.")
        continue
   
    # Λήψη νομισμάτων
    from_currency = input("Εισάγετε το νόμισμα από το οποίο θέλετε να μετατρέψετε (π.χ. USD): ").upper()
    to_currency = input("Εισάγετε το νόμισμα/τα στο οποίο θέλετε να μετατρέψετε (π.χ. EUR): ").upper()

    # Κλήση της συνάρτησης μετατροπής
    result = convert_currency(amount, from_currency, to_currency)
   
    # Εμφάνιση αποτελέσματος
    if isinstance(result, str):
        print(result)  # Αν είναι μήνυμα λάθους
    else:
        print(f"Το ποσό των {amount} {from_currency} ισούται με {result} {to_currency}.")

    # Μετά την μετατροπή ή μετά την εσφαλμένη εισαγωγή νομίσματος ο χρήστης ερωτείται αν θέλει να κάνει νεα μετατροπή. Αν δεν απαντήσει 'ναι' τότε ο μετατροπέας σταματάει να τρέχει,αλλιώς ξεκινάει εκ νέου.
    again = input("Θέλετε να κάνετε άλλη μετατροπή; (ναι/όχι): ").strip().lower()
    if again != "ναι":
        print("Ευχαριστούμε που χρησιμοποιήσατε τον μετατροπέα νομισμάτων!")
        break


# Κλήση της συνάρτησης για αλληλεπίδραση με τον χρήστη
user_input()
